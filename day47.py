# Create a program that imports the math module and uses its functions.

import math

print(math.sqrt(16))  # square root of 16
print(math.ceil(3.4))  # round up to the nearest integer
print(math.floor(3.4))  # round down to the nearest integer
print(math.pi)  # the value of pi
print(math.e)  # the value of e
print(math.pow(2, 3))  # 2 raised to the power of 3
print(math.log(2))  # the natural logarithm of 2
print(math.log10(2))  # the base-10 logarithm of 2
print(math.sin(0))  # the sine of 0
print(math.cos(0))  # the cosine of 0
print(math.tan(0))  # the tangent of 0
print(math.degrees(0))  # convert 0 radians to degrees
print(math.radians(0))  # convert 0 degrees to radians
print(math.factorial(5))  # the factorial of 5
print(math.gcd(10, 5))  # the greatest common divisor of 10 and 5
print(math.isfinite(10))  # check if 10 is finite
print(math.isinf(10))  # check if 10 is infinite
print(math.isnan(10))  # check if 10 is not a number
print(math.isclose(10, 10))  # check if 10 is close to 10
print(math.isqrt(10))  # the integer square root of 10
print(math.exp(10))  # e raised to the power of 10
print(math.expm1(10))  # e raised to the power of 10, minus 1
print(math.log1p(10))  # the natural logarithm of 10, plus 1
print(math.atan(10))  # the arctangent of 10
print(math.atan2(10, 10))  # the arctangent of 10/10
print(
    math.hypot(10, 10)
)  # the hypotenuse of a right-angled triangle with sides of 10 and 10
print(math.cosh(10))  # the hyperbolic cosine of 10
print(math.sinh(10))  # the hyperbolic sine of 10
print(math.tanh(10))  # the hyperbolic tangent of 10
print(math.erf(10))  # the error function of 10
print(math.erfc(10))  # the complementary error function of 10
print(math.gamma(10))  # the gamma function of 10
print(
    math.lgamma(10)
)  # the natural logarithm of the absolute value of the gamma function of 10
